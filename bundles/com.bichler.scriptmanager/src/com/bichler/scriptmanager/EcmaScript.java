package com.bichler.scriptmanager;

public class EcmaScript {

	private String name;
	private String script;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getScript() {
		return script;
	}

	public void setScript(String script) {
		this.script = script;
	}
}
